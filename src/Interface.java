    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
         DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
         int row = jTable1.getSelectedRow();
         String value = String.valueOf(jTable1.getValueAt(row,3));
         double total = Double.parseDouble(value);
         double fulltotal =Double.parseDouble(jLabel6.getText());
         double result = fulltotal - total;
         jLabel6.setText(String.valueOf(result));
         dtm.removeRow(row);
         
    }//GEN-LAST:event_jButton1ActionPerformed

