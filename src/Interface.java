    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
       String item =String.valueOf (jComboBox1.getSelectedItem());
       String price= jTextField1.getText();
       String qty=jTextField2.getText();
       String total=jLabel8.getText();
       
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.insertRow(jTable1.getRowCount(),new Object[]{item,price , qty ,total});
        
        double totall= Double.parseDouble(jLabel8.getText());
       double fulltotal=Double.parseDouble(jLabel6.getText());
        double result = totall + fulltotal;
        jLabel6. setText(String.valueOf (result));
        
        
        jTextField1. setText(null);
         jTextField2.setText(null);
         jLabel8.setText("0");
        
         
         
       
    }//GEN-LAST:event_jButton2ActionPerformed

